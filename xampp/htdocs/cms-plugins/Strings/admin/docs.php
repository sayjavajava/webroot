<h1 class="plugin-header">String Editor</h1>
<hr style="height: 1px; margin-bottom: 15px;"/>
<p><b>Overview</b><br/>
The String Editor lets you easily change various text items throughout the site without having to go in and edit each template page.</p>
<ul>
<li><a href="#add">How do I add site text?</a></li>
<li><a href="#edit">How do I edit site text?</a></li>
<li><a href="#delete">How do I delete site text?</a></li>
</ul>
<a name="add"></a>
<p style="font-weight: bold;">How do I add site text?</p>
Note: Just adding a text item to the site won't do anything. If the text code is not referenced anywhere in the page templates the text item will go unused.
<ol>
	<li>Open the String Editor section from the menu on the left</li>
	<li>Click on Add</li>
	<li>Select the language that this text will be saved for. (the default languages is automatically chosen)</li>
	<li>Change any auto translate options if desired</li>
	<li>Type the text code ie. [TEXT_CODE]</li>
	<li>Type the text for this code ie. Text Code</li>
	<li>If you want to save the page in multiple languages, click on the Translate button. Skip this step if it's not needed.</li>
	<li>Click on 'Save Text Item'</li>
</ol>

<a name="edit"></a>
<p style="font-weight: bold;">How do I edit site text?</p>
<p>If you need to edit language specific items, select the language from the list dropdown and click 'Search&raquo;'</p>
<ol>
	<li>Open the String Editor section from the menu on the left</li>
	<li>Click on List</li>
	<li>From the listing, move your mouse over the item you wish to edit and click on the 'Edit' link.</li>
	<li>Make any necessary changes</li>
	<li>Translate if necessary</li>
	<li>Click on 'Save Text Item'</li>
</ol>

<p>Alternately, for quick updates - especially if correcting translations</p>
<ol>
	<li>Open the String Editor section from the menu on the left</li>
	<li>Click on List</li>
	<li>From the listing, double click the text you want to change. A text box will appear with the text in it.</li>
	<li>Make any necessary changes</li>
	<li>While your cursor is still in the text box, hit the 'Enter' key on your keyboard to save.</li>
</ol>


<a name="delete"></a>
<p style="font-weight: bold;">How do I delete site text?</p>
Note: If you delete a site text item that's still used in a template, the text that was previously shown will now show up as the text code.<br/><br/>
You can delete items two different ways. <br/>
<br/>
Single Delete<br/>
<ol>
	<li>Open the String Editor section from the menu on the left</li>
	<li>Click on List</li>
	<li>From the listing, move your mouse over the item you wish to delete and click on the 'Delete' link.</li>
	<li>You will receive a confirmation window making sure you want to delete. Click on Yes if you're sure.</li>
	<li>The item has been deleted</li>
</ol>

<br/>
Bulk Delete<br/>
<ol>
	<li>Open the String Editor section from the menu on the left</li>
	<li>Click on List</li>
	<li>Select the checkboxes next to each item you want to delete</li>
	<li>Select Delete from the Bulk Action drop down</li>
	<li>Click on the Go button</li>
	<li>You will receive a confirmation window making sure you want to delete. Click on Yes if you're sure.</li>
	<li>The items have been deleted</li>
</ol>
